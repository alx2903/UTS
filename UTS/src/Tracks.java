
public class Tracks {
	String Song;
	String Genre;
	
	
	public Tracks(String song, String genre) {
		super();
		Song = song;
		Genre = genre;
	}
	
	public String getSong() {
		return Song;
	}
	public void setSong(String song) {
		Song = song;
	}
	public String getGenre() {
		return Genre;
	}
	public void setGenre(String genre) {
		Genre = genre;
	}
	
	
}
